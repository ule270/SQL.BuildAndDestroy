Insert a new record in the **Students** table.

**Schema:**
```
StudentName,
Address,
City,
PostalCode,
Country
```
**Record's info to enter:**
```
Jane Doe
57 Union St
Glasgow, Scotland
G13RB
```

CREATE TABLE Students(
StudentName VARCHAR(255)(Jane Doe),
Address VARCHAR(255)(57 Union St),
City VARCHAR(255) (Glasgow),
PostalCode VARCHAR(255) (G13RB),
Country VARCHAR(255) (Scotland),
);

